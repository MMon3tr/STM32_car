#include "headfile.h"

GPIO_TypeDef *gpio_index[3] = { GPIOA , GPIOB , GPIOC };

//-------------------------------------------------------------------------------------------------------------------
// @brief		GPIO๏ฟฝ๏ฟฝสผ๏ฟฝ๏ฟฝ
// @param	  GPIOn	  ัก๏ฟฝ๏ฟฝGPIO๏ฟฝหฟ๏ฟฝ	
// @param	  Pinx    ัก๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝลบ๏ฟฝ
// @param	  mode    ัก๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤฃสฝ
// @return		void  
// Sample usage:		gpio_init(GPIO_A,Pin_0,OUT_PP);
//-------------------------------------------------------------------------------------------------------------------
void gpio_init(GPIOn_enum GPIOn,Pinx_enum Pinx,GPIO_MODE_enum mode)
{
	uint32_t temp;
	
	RCC->APB2ENR |= 4<<GPIOn;         //สน๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝ
  
	if(Pinx<8)
	{
			gpio_index[GPIOn]->CRL &= ~((0x0000000F)<<(Pinx*4));     //๏ฟฝ๏ฟฝ๏ฟฝึผฤด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝัก๏ฟฝ๏ฟฝฮป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
			if(mode == OUT_PP || mode == AF_PP || mode == OUT_OD)
				gpio_index[GPIOn]->CRL |= ((0x00000003)|(1<<mode))<<(Pinx*4);        //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ(ฤฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝอต๏ฟฝฦฝ)
			else if(mode == IU || mode == ID)
			{
				gpio_index[GPIOn]->CRL |= 0x00000008<<(Pinx*4);        //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ/๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
				if(mode == IU)
				{
					temp = gpio_index[GPIOn]->ODR&(~(1<<Pinx));          //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝัก๏ฟฝ๏ฟฝฮป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
					gpio_index[GPIOn]->ODR = temp|(1<<Pinx);             //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝัก๏ฟฝ๏ฟฝฮป๏ฟฝ๏ฟฝ1
				}
				if(mode == ID)
					gpio_index[GPIOn]->ODR &= ~(1<<Pinx);                //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝัก๏ฟฝ๏ฟฝฮป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
			}
			else
				gpio_index[GPIOn]->CRL |= (0x00000004<<mode)<<(Pinx*4);								
	}
	else
  {
			gpio_index[GPIOn]->CRH &= ~((0x0000000F)<<((Pinx-8)*4));     //๏ฟฝ๏ฟฝ๏ฟฝึผฤด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝัก๏ฟฝ๏ฟฝฮป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
			if(mode == OUT_PP || mode == AF_PP || mode == OUT_OD)
				gpio_index[GPIOn]->CRH |= ((0x00000003)|(1<<mode))<<((Pinx-8)*4);        //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ(ฤฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝอต๏ฟฝฦฝ)
			else if(mode == IU || mode == ID)
			{
				gpio_index[GPIOn]->CRH |= 0x00000008<<((Pinx-8)*4);        //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ/๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
				if(mode == IU)
				{
					temp = GPIOA->ODR&(~(1<<Pinx));          //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝัก๏ฟฝ๏ฟฝฮป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
					gpio_index[GPIOn]->ODR = temp|(1<<Pinx);             //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝัก๏ฟฝ๏ฟฝฮป๏ฟฝ๏ฟฝ1
				}
				if(mode == ID)
					gpio_index[GPIOn]->ODR &= ~(1<<Pinx);                //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝัก๏ฟฝ๏ฟฝฮป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
			}
			else
				gpio_index[GPIOn]->CRH |= (0x00000004<<mode)<<((Pinx-8)*4);	
			
		}

}

//-------------------------------------------------------------------------------------------------------------------
// @brief		๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝGPIO๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฦฝ
// @param	  GPIOn	  ัก๏ฟฝ๏ฟฝGPIO๏ฟฝหฟ๏ฟฝ	
// @param	  Pinx    ัก๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝลบ๏ฟฝ
// @param	  mode    ๏ฟฝ๏ฟฝ1ฮช๏ฟฝ฿ต๏ฟฝฦฝ ๏ฟฝ๏ฟฝ0ฮช๏ฟฝอต๏ฟฝฦฝ
// @return		void  
// Sample usage:			gpio_set(GPIO_A,Pin_0,1);
//-------------------------------------------------------------------------------------------------------------------
void gpio_set(GPIOn_enum GPIOn,Pinx_enum Pinx,uint8_t mode)
{
	/* ไฝฟ็จBSRRๅๅญๅ, ้ฟๅODR่ฏป-ๆน-ๅ็ซๆ(ISRๅไธปๅพช็ฏๅฏ่ฝๅๆถๅๅไธGPIOๅฃ็ไธๅๅผ่) */
	if(mode)
		gpio_index[GPIOn]->BSRR = (uint32_t)1 << Pinx;           /* BSx: ็ฝฎไฝ */
	else
		gpio_index[GPIOn]->BSRR = (uint32_t)1 << (Pinx + 16);    /* BRx: ๅคไฝ */
}

//-------------------------------------------------------------------------------------------------------------------
// @brief		๏ฟฝ๏ฟฝศกGPIO๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
// @param	  GPIOn	  ัก๏ฟฝ๏ฟฝGPIO๏ฟฝหฟ๏ฟฝ	
// @param	  Pinx    ัก๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝลบ๏ฟฝ
// @return		uint8_t  
// Sample usage:			gpio_get(GPIO_A,Pin_4)
//-------------------------------------------------------------------------------------------------------------------
uint8_t gpio_get(GPIOn_enum GPIOn,Pinx_enum Pinx)
{
	uint32_t temp;
	
	temp = gpio_index[GPIOn]->IDR&(1<<Pinx);   //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮปศซ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ0 ัก๏ฟฝ๏ฟฝฮปฮช1๏ฟฝ๏ฟฝtemp๏ฟฝ๏ฟฝฮช0 ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝtempฮช0
	
	if(temp)                          //temp๏ฟฝ๏ฟฝฮช0 หต๏ฟฝ๏ฟฝัก๏ฟฝ๏ฟฝฮปฮช1 ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ฿ต๏ฟฝฦฝ
		return 1;
	else                              //tempฮช0 หต๏ฟฝ๏ฟฝัก๏ฟฝ๏ฟฝฮปฮช0 ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝอต๏ฟฝฦฝ
		return 0;
}
